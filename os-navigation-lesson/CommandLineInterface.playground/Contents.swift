 ## Command Line Interface
 * A CLI (command line interface) is a user interface to a computer's operating system or an application in which the user responds to a visual prompt by typing in a command on a specified line, receives a response back from the system, and then enters another command, and so forth
 
 ### Syntax of a command
 * **command** _option(s)_ _filename(s)_
 
 ![command](command.png)
 
 * A UNIX command may or may not have arguments. An argument can be an option or a filename.
 * UNIX command lines can be simple, one-word entries like the date command. They can also be more complex: you may need to type more than the command name.
 
